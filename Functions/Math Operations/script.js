function sumOfNumbers(numberOne, numberTwo){
    let result = parseFloat (numberOne) + parseFloat(numberTwo);
    console.log ('The sum is:', result)
}

function quotientOfNumbers(numberOne, numberTwo){
    let result = parseFloat (numberOne) / parseFloat(numberTwo);
    console.log ('The quotient is:', result)
}


function productOfNumbers(numberOne, numberTwo){
    let result = parseFloat (numberOne) * parseFloat(numberTwo);
    console.log ('The product is:', result)
}



function differenceofNumbers(numberOne, numberTwo){
    let result = parseFloat (numberOne) - parseFloat(numberTwo);
    console.log ('The difference is:', result)
};

